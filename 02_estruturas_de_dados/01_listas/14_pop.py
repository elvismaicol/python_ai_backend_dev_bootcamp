# Retira o último elemento da lista por padrão (pilha), 
# ou pode ser passado como argumento a posição do objeto

linguagens = ["python", "js", "c", "java", "csharp"]

print(linguagens.pop())  
# csharp

print(linguagens.pop())  
# java

print(linguagens.pop())  
# c

print(linguagens.pop(0))  
# python